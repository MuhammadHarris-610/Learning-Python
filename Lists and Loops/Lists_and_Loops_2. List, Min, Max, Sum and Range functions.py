# Lists and Loops: Exercise 2 (Using list, min, max and sum functions)

# Program Description: Making a list of number 1-1000 and performing various operations like maximum, minimum numbers and sum of the numbers in the list.

numbers = list(range(1, 1001)) # Used the list function to create the list data structure, while used the range function to add in the values from 1-1000.
print(numbers)
print(min(numbers))
print(max(numbers))
print(sum(numbers))