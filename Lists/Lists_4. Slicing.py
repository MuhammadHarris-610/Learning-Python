# Program Description: Holy Sites
locations = ['Mecca', 'Medina', 'Jerusalem', 'Damascus', 'Hebron', 'Kufa', 'Basra', 'Antioch']
print(f'The first three items in the list are {locations[:3]}') # Created a temporary list containing the items 0-2 of the original list.
print(f'Four items from the middle of the list are {locations[2:-2]}') # Created a temporary list containing items 2- third last item of the original list.
print(f'The last three items in the list are {locations[-3:]}') # Created a temporary list containing the items third last item - last item of the original list.